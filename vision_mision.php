      <h1>Misión</h1>
      <p>
        Promover el desarrollo artístico-cultural de la comuna, que permita la participación activa y el acceso de la comunidad, a una oferta amplia de bienes y servicios culturales de excelencia artística y técnica, que integre y. brinde una planificación local con una gestión moderna y eficiente, para la implementación de iniciativas innovadoras en una infraestructura de primer nivel. Nuestros objetivos estratégicos priorizan la integración de la comunidad, con énfasis en los grupos vulnerables, localidades apartadas y rurales; la formación y desarrollo de habilidades artísticas y de audiencias, mediante talleres de diversas expresiones artísticas; la difusión efectiva de nuestras actividades y el rescate, salvaguarda y promoción de nuestro patrimonio cultural local.
      <h1>Misión</h1>
      <p>
        Posicionar al Centro Cultural de Colina como una institución cercana, innovadora y activa. Gracias al reconocimiento y la participación de la comunidad, en una amplia oferta cultural (exposiciones, cine club, muestras, visitas patrimoniales, charlas, talleres, etc.), que contempla diversas expresiones y prácticas culturales, de gran calidad técnica y artística (artes visuales, música, cine, teatro, literatura, patrimonio, identidad, entre otras). Esto, en una infraestructura de alto nivel, a disposición de la comunidad para su uso y dinamización. Asimismo, es nuestro propósito garantizar la asociatividad entre artistas, artesanos y agrupaciones culturales, en una red que integre a las localidades rurales, las zonas apartadas y altamente pobladas, estableciéndonos como un polo estratégico de desarrollo artístico-cultural en la provincia.
